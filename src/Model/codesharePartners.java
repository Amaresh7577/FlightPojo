package Model;

public class codesharePartners {
	private String marketedCarrierCode;
	private String marketedCarrierName;
	private String marketedFlightNumber;
	
	public String getMarketedCarrierCode() {
		return marketedCarrierCode;
	}
	public void setMarketedCarrierCode(String marketedCarrierCode) {
		this.marketedCarrierCode = marketedCarrierCode;
	}
	public String getMarketedCarrierName() {
		return marketedCarrierName;
	}
	public void setMarketedCarrierName(String marketedCarrierName) {
		this.marketedCarrierName = marketedCarrierName;
	}
	public String getMarketedFlightNumber() {
		return marketedFlightNumber;
	}
	public void setMarketedFlightNumber(String marketedFlightNumber) {
		this.marketedFlightNumber = marketedFlightNumber;
	}
	
	
}
